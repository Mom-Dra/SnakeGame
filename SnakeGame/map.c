void IncreaseItemCount(int x, int y)
{
	++ItemCount;

	for (int i = 0; i < ITEM_ARR_LENGTH; ++i)
	{
		if (isInclude[i] == 0)
		{
			items[i].x = x;
			items[i].y = y;

			isInclude[i] = 1;

			break;
		}
	}
}

void DecreaseItemCount(struct Vector2 pos)
{
	--ItemCount;

	for (int i = 0; i < ITEM_ARR_LENGTH; ++i)
	{
		if (items[i].x == pos.x && items[i].y == pos.y)
		{
			isInclude[i] = 0;
			break;
		}
	}
}

int IsExistItem()
{
	return ItemCount;
}

int* GetIsIncluded()
{
	return isInclude;
}

struct Vector2* GetItems()
{
	return items;
}